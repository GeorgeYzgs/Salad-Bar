package classes;

/**
 *
 * @author George.Giazitzis
 */
public class Ingredient {

    private final String name;
    private final float price;

    public Ingredient(String name, float price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public float getPrice() {
        return price;
    }
}
